DATA SEGMENT 

MSG DB 10,13,"ADDITION$"
MSG1 DB 10,13,"ENTER FIRST NUMBER :$"
MSG2 DB 10,13,"ENTER SECOND NUMBER :$"
MSG3 DB 10,13,"THE SUM IS :$"

DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,MSG
	INT 21H
	
	MOV AH,09H
	LEA DX,MSG1
	INT 21H
	
	MOV AH,01H
	INT 21H
	MOV BL,AL
	SUB BL,30H
	
	MOV AH,09H
	LEA DX,MSG2
	INT 21H
	
	MOV AH,01H
	INT 21H
	MOV BH,AL
	SUB BL,30H
	
	ADD BH,BL
	ADD BH,30H
	
	MOV AH,09H
	LEA DX,MSG3
	INT 21H
	
	MOV DL,BH
	MOV AH,02H
	INT 21H	
	
	MOV AH,4CH
	INT 21H

CODE ENDS
END START
